'''Python 3 has several built-in data types that allow you to represent and manipulate different kinds of data. Here are some of the commonly used data types in Python:

1. Numeric Types:
   - Integer (`int`): Represents whole numbers, both positive and negative, without any fractional part.
   - Float (`float`): Represents real numbers with decimal points, allowing fractional parts.

Example:'''
x = 10  # Integer
y = 3.14  # Float
'''
2. String (`str`): Represents a sequence of characters enclosed in either single quotes ('') or double quotes (""). Strings are used to store and manipulate textual data.

Example:'''
name = "John"  # String
message = 'Hello, world!'

'''3. Boolean (`bool`): Represents a truth value, which can be either `True` or `False`. Booleans are typically used in conditions and logical operations.

Example:'''
is_true = True  # Boolean
is_false = False
'''
4. List (`list`): Represents an ordered collection of items, which can be of different data types. Lists are mutable, meaning you can modify their elements after they are created. Lists are enclosed in square brackets ([]), and the elements are separated by commas.

Example:'''
numbers = [1, 2, 3, 4, 5]  # List
fruits = ['apple', 'banana', 'orange']
'''
5. Tuple (`tuple`): Represents an ordered collection of items, similar to a list. However, tuples are immutable, meaning their elements cannot be modified once they are defined. Tuples are enclosed in parentheses (()), and the elements are separated by commas.

Example:'''
point = (2, 5)  # Tuple
colors = ('red', 'green', 'blue')

'''6. Dictionary (`dict`): Represents a collection of key-value pairs. Each element in a dictionary consists of a key and its associated value. Dictionaries are enclosed in curly braces ({}) and use colons (:) to separate keys and values.

Example:'''
student = {'name': 'John', 'age': 20}  # Dictionary
book = {'title': 'Python Crash Course', 'author': 'Eric Matthes'}

'''These are just some of the built-in data types in Python 3. Python also supports additional data types such as sets, frozen sets, byte arrays, and more. Additionally, you can create and work with custom data types using classes and objects in object-oriented programming.'''